  <p>
    Nếu bạn là những tín đồ yêu thích những quyển tiểu thuyết cổ điển, chính trị
    của các nước phương tây thì đừng bỏ qua những tác phẩm sau đây. Những cuốn
    sách đã nói lên sự thật ẩn chứa trong xã hội xưa một cách chân thật nhất. Từ
    con người, xã hội và hoàn cảnh, nội tâm nhân vật được miêu tả sâu sắc, phản
    ánh rõ được các giai cấp. <br /><br />

    Bà Bovary - Gustave Flaubert<br /><br />

    Đây là câu chuyện thời sự kể lên việc ngoại tình của vợ một viên thầy thuốc,
    Gustave Flaubert đã viết tiểu thuyết nổi tiếng Bà Bovary, xuất bản năm 1856.
    Quyển tiểu thyết đã khắc hoạ về một thiếu phụ với cuộc hôn nhân không như ý,
    người chồng tầm thường, những gã tình nhân ích kỷ, những mơ ước, ảo tưởng bị
    bóp nghẹt.<br /><br />

    Nhưng không chỉ có vậy, cuốn sách còn cất lên tiếng nói phản kháng của cá
    nhân chống lại những quy tắc xã hội và đạo đức của thế giới tư sản chật hẹp
    và giả dối. Đồng thời miêu tả quá trình tan vỡ của những thơ mộng lãng mạn
    mà cá nhân khao khát hạnh phúc đặt vào cái thực tại tầm thường và hèn kém
    của cuộc sống hằng ngày.<br /><br />

    <img src="public/img/news/pasted image 0 (4).png" alt="" />
    Tất cả các dòng sông đều chảy - Nancy cato<br /><br />

    Nancy Cato đương còn là một cái tên rất lạ đối với độc giả Việt Nam. Tất cả
    các dòng sông đều chảy cũng không nằm trong top những tác phẩm văn học kinh
    điển thế giới như Cuốn theo chiều gió, Tiếng chim hót trong bụi mận gai,
    Bông hồng vàng và bình minh mưa,…<br /><br />

    Nhưng tôi lại yêu cuốn sách nhỏ này bằng một tình yêu kì bí, tình yêu mà
    chính tôi cũng không sao giải thích được. Từ lần đầu tiên bắt gặp Tất cả các
    dòng sông đều chảy ở cửa hàng sách, tôi đã lập tức bị cuốn hút bởi nó, và
    quay lại ngay hôm sau để mua về bằng được. Có lẽ, đó cũng là một cơ duyên,
    cơ duyên của tôi với văn chương…<br /><br />

    <img src="public/img/news/pasted image 0 (5).png" alt="" />
    Con hủi - Helena Mniszek<br /><br />

    Con Hủi là tác phẩm văn học kinh điển tiêu biểu và nổi tiếng nhất của Helena
    Mniszek, một câu chuyện tình thơ mộng và đắm say, trong sáng và mãnh liệt,
    tươi đẹp nhưng thảm thê của đôi thanh niên tài sắc, cưỡng chống lại những
    ràng buộc của đẳng cấp quí tộc để bảo vệ hạnh phúc mà họ đã chọn.<br /><br />

    Chàng - đại công tước Valđemr Mikhôrôvxki - là một thanh niên quí tộc thuộc
    dòng họ quyền quí nhất nước, nhiều thế hệ từng đảm nhiệm những cương vị cao
    sang. Chàng có học thức, giàu có, quảng giao, nhiệt thành, can trường, mới
    mẻ trong tư duy, cả quyết trong hành động, say sưa và nồng nhiệt trong tình
    yêu.<br /><br />

    Nàng - Xtefchia Ruđexka - chỉ là con gái một điền chủ nhỏ, thất vọng vì mối
    tình đã rời gia đình đến làm gia sư cho cô ruột của chàng. Cảm vì sắc, mến
    vì tài, lại mang trong tâm thức niềm khao khát của cuộc tình truyền kiếp bất
    hạnh từ đời trước (ông nội của chàng và bà ngoại của nàng từng yêu tha thiết
    nhưng không lấy được nhau), đôi trẻ đã vượt qua những hiểu lầm đầu tiên, cảm
    nhận được giá trị đích thực của nhau và đến với nhau. Trải qua bao đấu tranh
    gay go, với gia đình, xã hội và với chính mình để bảo vệ tình yêu, tình yêu
    của họ đã thắng: họ được làm lễ đính ước và chuẩn bị cho ngày cưới.<br /><br />

    <img src="public/img/news/pasted image 0 (6).png" alt="" />
    Kiêu hãnh và định kiến - Jane Austen<br /><br />

    Kiêu hãnh và định kiến là truyện được yêu thích nhất trong số các tác phẩm
    của Jane Austen. Truyện kể về cuộc đối đầu giữa Elizabeth Bennet, con gái
    của một gia đình trung lưu, và Fitzwilliam Darcy, một địa chủ giàu có.<br /><br />

    Mặc dù họ để tâm tìm hiểu lẫn nhau, tác giả đã đảo ngược ý niệm thông thường
    về "thuở ban đầu": tính kiêu hãnh về giai cấp và tài sản của mình cùng định
    kiến về vị thế thấp kém của gia đình Elizabeth khiến Darcy lúc đầu muốn
    tránh xa cô, trong khi cô Elizabeth cũng kiêu hãnh vì lòng tự trọng của mình
    và có định kiến về cung cách trưởng giả của Darcy, trở nên một người con gái
    không giống như những người con gái khác chung quanh Darcy.<br /><br />

    Elizabeth, với cá tính nhạy bén, dí dỏm và cứng cỏi, được xem là một trong
    những nhân vật lôi cuốn nhất trong nền văn học Anh.<br /><br />

