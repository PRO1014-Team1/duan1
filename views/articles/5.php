  <p>
    Sách là người bạn của trí tuệ và là thứ sẽ đưa chúng ta đến những tầng cao
    mới. Vì sách là hội tụ những tri thức mà tri thức là sức mạnh. Khi bạn có
    một khối tri thức khổng lồ, bạn sẽ nâng cao được giá trị của bản thân. Thậm
    chí khi có tri thức bạn có thể giúp được cả những người xung quanh mình. Hãy
    đọc bài viết sau để hiểu được giá trị cũng như định hình thói quen đọc sách,
    phương pháp đọc tối ưu nhất nhé. <br /><br />

    Xác định rõ mục tiêu kiến thức<br /><br />

    Khi bạn còn nhỏ, thì bạn có thể đọc đa dạng các loại sách nhưng lớn hơn thì
    bạn cần rõ ràng trong việc chọn lựa sách. Vì thời gian bạn không còn nhiều
    và bạn cần phải tập trung thì sẽ tốt hơn. Bạn sẽ có 2 vấn đề đó là kiến thức
    chuyên môn và kiến thức xã hội. Hai dòng kiến thức này sẽ hỗ trợ bạn rất
    nhiều trong công việc cũng như cuộc sống.<br /><br />

    Những loại sách có kiến thức chuyên môn thường khô khan và sẽ khiến bạn nhàm
    chán. Vì thế, bạn nên có thêm 1 quyển khác gối đầu giường để giúp mình thư
    giãn. Quyển đó như là một sự đam mê, yêu thích của mình, gọi chung là kiến
    thức xã hội, đời sống. Hãy xác định mục tiêu trước, mình muốn tìm hiểu về
    điều gì? Mình cần nâng cấp chuyên môn nào?<br /><br />

    Mỗi giai đoạn là mỗi quyển sách khác nhau<br /><br />

    Luyện tập thói quen đọc sách mỗi không chỉ đơn thuần là đọc rồi ghi chú hay
    khô cứng mà nó còn phụ thuộc vào tuỳ giai đoạn. Một ví dụ điển hình là khi
    bạn đang chán đời, chán việc, bắt bạn đọc nghiên cứu thêm về công việc thì
    sao mà nuốt trôi. Đó là cảm xúc, và con người chúng ta sẽ luôn bị cảm xúc
    chi phối.<br /><br />

    Khi bạn uể oải, stress và mất hết năng lượng hãy dùng những quyển sách self
    help, nó có thể sẽ vực bạn dậy. Tuy nhiên, liều thuốc bổ không được dùng quá
    nhiều, nếu không bạn sẽ bị lạm dụng. Khi đó, bạn sống trong viển vông. Đừng
    để chúng ta sa lầy quá nhiều vào những quyển sách self help. Ngoài ra, bạn
    nên đọc tiểu thuyết vì nó là thứ nói lên rất nhiều thứ mà xã hội hiện thực
    không có.<br /><br />

    Tạo văn hoá đọc mỗi ngày<br /><br />

    Đây là điều rất quan trọng cho những người đọc sách. Nếu từ đầu bạn không
    yêu đọc sách thì bắt buộc phải làm chuyện này. Vì nếu không, bạn sẽ rất ngán
    ngẩm và bỏ giữa chừng. Chính điều đó nên đa số ta cần đọc cái ta thích
    trước, sau đó ta sẽ đọc cái ta cần. Khi bạn quen với chữ, với kiểu ngồi hàng
    giờ cầm sách thì cần phải tập.<br /><br />

    Những cuốn sách ban đầu bạn đọc để tạo ra văn hoá đọc. Bạn cần chọn lọc sách
    nhẹ nhàng, tiểu thuyết, những thứ làm bạn say mê. Rồi mỗi ngày dành ra bao
    nhiêu thời gian để đọc, ghi chú thời gian rõ ràng. Hãy hình thành thói
    quen.<br /><br />

    Ghi chú hoặc gấp dấu<br /><br />

    Phương pháp đọc sách này được nhiều người sử dụng khi đọc các quyển sách
    chuyên ngành. Bạn cần ghi chú lại ra nháp để nhớ hoặc tra cứu. Ví dụ bạn đọc
    sách về luật, có một số thuật ngữ chuyên ngành là “đương đơn, bị cáo, giấy
    triệu tập,...” bạn không hiểu thì ghi lại. Sau đó lên google tra các khái
    niệm, là bạn sẽ hiểu hết được khúc đó nói gì. Ngoài ra, bạn cũng nên dùng
    bút dạ quang để đánh dấu các keyword. Có những trang cần gấp dấu thì chú ý
    ghi số ở quyển sổ tay.<br /><br />

    Nhẩm lại ý nghĩa chính sau khi đọc<br /><br />

    Bạn chỉ cần bỏ ra 10 phút để nhớ lại những keyword chính, điểm cốt yếu. Nó
    giống như việc bạn hệ thống lại bộ não của mình về kiến thức đó. Hãy củng cố
    lại tất cả những gì bạn đọc được. Đặc biệt đối với những loại sách chuyên
    ngành. Bạn có thể ghi chú hoặc không, tốt nhất hãy lưu lại vào bộ não. Như
    vậy, dẫu có quên thì bạn cũng sẽ có thể nhớ lại dễ dàng khi cần hoặc ai đó
    nhắc lại.<br /><br />

    Thói quen đọc sách mỗi ngày sẽ giúp bạn nâng cao tri thức, chất lượng cuộc
    sống. Đây là những “liều thuốc bổ não” để giúp bạn trở nên tốt hơn. Những
    quyển sách dù như thế nào đi nữa, ít nhiều gì cũng sẽ có điều gì đó cho bạn
    học hỏi. Thậm chí trong 1 quyển sách có 1 trang làm thay đổi suy nghĩ của
    bạn thì hãy mua nó.<br /><br />

    “Một trang sách trong một quyển thay đổi 1% con người thì một quyển sẽ thay
    đổi cả cuộc đời bạn.”<br /><br />
