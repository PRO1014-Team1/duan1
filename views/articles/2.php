  <p>
    Các tác phẩm văn học Việt Nam từ xưa đến nay phong phú và đang dạng. Bên
    cạnh đó, có nhiều loại tiểu thuyết mô tả tính hiện thực sâu sắc, gắn liền
    với những con người rất bình thường. Từ đó, bạn đọc có thể hiểu rõ xã hội
    thực và tâm lý, hoàn cảnh, văn hoá của con người và xã hội. <br /><br />

    1. Gió lạnh đầu mùa - Thạch Lam<br /><br />

    Tác phẩm mang tên Thạch Lam đặc biệt rất đơn giản. Thông thường chỉ là những
    câu chuyện không phải chuyện, nhẹ nhàng như một đoạn phim ngắn. Tác giả muốn
    thông qua tâm trạng nhân vật để có thể phản chiếu một cách chân thật nhất về
    mỗi câu chuyện hằng ngày. Cuộc sống của nhân vật với những sự rất đỗi bình
    thường.<br /><br />

    Suy nghĩ của Thạch Lam về văn chương: “Đối với tôi văn chương không phải là
    một cách đem đến cho người đọc sự thoát li hay sự quên, trái lại văn chương
    là một thứ khí giới thanh cao và đắc lực mà chúng ta có, để vừa tố cáo và
    thay đổi một cái thế giới giả dối và tàn ác, làm cho lòng người được thêm
    trong sạch và phong phú hơn.”<br /><br />

    Giống với Hai đứa trẻ, khi chị em Liên ngồi đợi những chuyến tàu từ Hà Nội,
    họ mải mê ngắm nhìn phố chợ về đêm, trong cái thưa thớt, vắng vẻ ấy mà thấu
    hiểu sự vất vả, tối tăm của cuộc sống thường ngày nhưng sâu tận đáy lòng vẫn
    luôn hướng đến một cuộc sống tốt đẹp hơn.<br /><br />

    <img src="./public/img/news/pasted image 0.png" alt="" />

    2. Tắt đèn - Ngô Tất Tố<br /><br />

    Đây là 1 trong những tác phẩm văn học Việt Nam kinh điển không thể bỏ qua.
    Ngô Tất Tố được coi là nhà văn hàng đầu của trào lưu hiện thực phê phán ở
    Việt Nam trước 1945, ông có nhiều tác phẩm để đời như Tắt đèn, Việc làng,
    Tập án cái đình, Lều chõng.<br /><br />

    Tắt đèn của nhà văn Ngô Tất Tố phản ánh rất chân thực cuộc sống khốn khổ của
    tầng lớp nông dân Việt Nam đầu thế kỷ XX dưới ách đô hộ của thực dân Pháp.
    Tác phẩm xoay quanh nhân vật chị Dậu và gia đình – một điển hình của cuộc
    sống bần cùng hóa sưu cao thuế nặng mà chế độ thực dân áp đặt lên xã hội
    Việt Nam.<br /><br />

    Trong cơn cùng cực chị Dậu phải bán khoai, bán bầy chó đẻ và bán cả đứa con
    để lấy tiền nộp sưu thuế cho chồng nhưng cuộc sống vẫn đi vào bế tắc, không
    lối thoát.<br /><br />

    <img src="./public/img/news/pasted image 0 (1).png" alt="" />
    3. Chí Phèo - Nam Cao<br /><br />

    Tác phẩm văn học Việt Nam “Chí Phèo” được nhà văn Nam Cao đặt tên trong bản
    thảo là “Cái lò gạch cũ”, nhưng có lẽ nhằm gây chú ý cho công chúng đương
    thời, nhà văn Lê văn Trương khi viết lời tựa cho tập truyện đã đổi thành
    “Đôi lứa xứng đôi”. Về sau, khi in lại truyện này trong tuyển tập “Luống
    cày” (tập truyện của 4 tác giả: Nam Cao, Nguyên Hồng, Nguyễn Huy Tưởng, Kim
    Lân) tác giả Nam Cao đổi tên truyện của mình thành “Chí Phèo”.<br /><br />

    Truyện ngắn Chí Phèo đã nói lên sự thực khốc liệt của xã hội phong kiến thực
    dân xưa, đã coi con người như cỏ rác. Sự đau lòng đến tận xương tuỷ, sự
    lương thiện bị vùi dập nhưng vẫn nhem nhóm và cháy lên trong từng chi
    tiết.<br /><br />

    “Cái chết của con Mực” cũng là trang viết về nỗi lòng lực bất tòng tâm của
    người trí thức nghèo trước thời cuộc; riêng hai truyện “Ma đưa” và “Chú Khì
    người đánh tổ tôm vô hình” thuộc loại truyện ma, trong đó Nam Cao ghi lại
    những nét thuộc đời sống tâm linh, tâm thức dị đoan của người dân các làng
    quê thời đấy.<br /><br />

