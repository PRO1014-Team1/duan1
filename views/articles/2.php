    4. Số Đỏ - Vũ Trọng Phụng<br /><br />

    SỐ ĐỎ Tác phẩm Số đỏ, tác giả Vũ Trọng Phụng(20/10/1912-13/10/1939)- “Ông
    vua phóng sự đất Bắc”. “Số đỏ” được đăng ở Hà Nội báo ngày 7/10/1936 được
    xem là tác phẩm lớn nhất trong sự nghiệp sáng tác văn học của Vũ Trọng
    Phụng, cùng với “ Giông tố” là thùng thuốc nổ ném vào giữa xã hội thực
    dân.<br /><br />

    “Số đỏ” xoay quanh nhân vật làm đảo điển Hà Nội những năm 1930-1940, Xuân
    Tóc Đỏ- từ một thằng bé mồ côi, kiếm sống bằng đủ thứ nghề: trèo me, trèo
    sấu, quảng cáo thuốc lậu...nhờ thủ đoạn xảo trá, “nhờ thời” đã trở thành me
    xừ, được mời vào hội Khai trí Tiến đức và cuối cùng được nhận huân chương
    Bắc Đẩu bội tinh.<br /><br />

    Sử dụng lối tương phản cái đồi bại, thối nát vô luân với cái hài, cái trào
    phúng, tiểu thuyết thành công trong việc lột trần những “quái thai” thời đại
    trong buổi giao thời.<br /><br />

